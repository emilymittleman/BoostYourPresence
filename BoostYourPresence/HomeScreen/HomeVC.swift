import UIKit

let INFINITE_SIZE = 10000000

class HomeVC: UIViewController, UITableViewDataSource {
    
    override func viewDidLoad() {
        super.viewDidLoad()

        // Do any additional setup after loading the view.
    }
    
    func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }
    
    func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return INFINITE_SIZE
    }
    
    func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "cell") as! SocialMediaRow
        cell.socialMediaCollectionView.tag = indexPath.section
        cell.socialMediaCollectionView.reloadData()
        
        return cell
    }
    

